 private String obterCampos() {
        String sql = "";
        String nome = "";
        String quantidade = "";
       

        if (acaComboNome.getSelectedIndex() >0 ){
            nome = "and matnome =  '"+acaComboNome.getSelectedItem()+"'";

        }
        if (!acaTextfieldQtde.getText().equals("")) {
            quantidade = "and matqtde = " + Double.parseDouble(acaTextfieldQtde.getText()) + "";
        }

        sql = "SELECT M1.MATNOME, M1.MATQTDE, M1.MATVALOR, T1.TIPOMATFOR, U1.UNITIPO "
                + " FROM MATERIAPRIMA M1, TIPOMATERIA T1, UNIDADEMAT U1 "
                + " WHERE T1.TIPOMATCOD = M1.TIPOMATCOD AND M1.UNICOD = U1.UNICOD " + nome + quantidade;

        return sql;

    }

