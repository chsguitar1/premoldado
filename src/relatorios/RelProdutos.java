  public String obterCampos() {
        String sql = "";
        String cidade = "";
        String nome = "";
        String status = "";
        String tipo = "";

        if (!(acaTextfieldNome.getText().equals(""))) {
            nome = " prodnome like '%" + acaTextfieldNome.getTexto() + "%' ";

        }


        if (acaComboStatus.getSelectedIndex() > 0) {
            if (acaTextfieldNome.getText().equals("")) {
                status = "  prodstatus = " + acaComboStatus.getValor() + " ";

            } else{
              status = "  and prodstatus = " + acaComboStatus.getValor() + " ";
            }
        }

        //sql = "select * from materiaprima " + tipo + " " + nome + status;
        sql = "select * from produto where " + nome + status;
        return sql;

    }
