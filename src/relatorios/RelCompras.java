    private String obterCampos() {
        String sql = "";
        String pedido = "";
        String nome = "";
        String data = "";
        String condicao = "";
        if (!acaTextfieldPedido.getText().equals("")) {
            pedido = "and c2.pecomcod = " + Integer.parseInt(acaTextfieldPedido.getText()) + "";
        }
        if (!(acaTextfieldNome.getText().equals(""))) {
            nome = " and f1.fonome like '%" + acaTextfieldNome.getTexto() + "%' ";

        }
        if (!acaDataChooserCompra.getValor().equals("")) {
            data = " and c2.comdata <= '" + acaDataChooserCompra.getValor() + "'";
        } else {
            data = "";
        }
        sql = "SELECT F1.FONOME,C3.CIDNOME,E1.ESTNOME,C2.COMDATA,C2.COMTOTAL,C2.PECOMCOD "
                + "FROM FORNECEDOR F1,CIDADE C3,ESTADO E1,COMPRA C2,PEDCOMPRA P1 "
                + "WHERE E1.ESTCOD = C3.ESTCOD AND C3.CIDCOD = F1.CIDCOD AND F1.FORCOD = P1.FORCOD AND P1.PECOMCOD = C2.PECOMCOD " + pedido + nome + data;
        return sql;

    }

