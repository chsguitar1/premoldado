    public String obterCampos() {
        String sql = "";
        String cidade = "";
        String nome = "";
        String status = "";
        String tipo = "";
        if (acaComboCid.getSelectedIndex() >= 0) {
            cidade = "cidcod = " + acaComboCid.getValor() + " ";
        }
        if (!(acaTextfieldNome.getText().equals(""))) {
            nome = " and fonome like '%" + acaTextfieldNome.getTexto() + "%' ";
        }
        if (acaComboStatus.getSelectedIndex() >= 0) {
            status = " and forstatus = " + acaComboStatus.getValor() + " ";
        }

        sql = "select * from fornecedor where " + cidade + nome + status + tipo;
        return sql;

    }

