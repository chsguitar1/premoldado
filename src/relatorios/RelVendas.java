    private String obterCampos() {
        String sql = "";
        String pedido = "";
        String nome = "";
        String data = "";
        String condicao = "";
        if (!acaTextfieldPedido.getText().equals("")) {
            pedido = " and v1.pedvencod = " + Integer.parseInt(acaTextfieldPedido.getText()) + "";
        }
        if (!(acaTextfieldNome.getText().equals(""))) {
            nome = " and clinome like '%" + acaTextfieldNome.getTexto() + "%' ";

        }
     
        sql = "SELECT V1.VENDATA, V1.VENPARCELAS, V1.VENTOTAL, C1.CLINOME "
                + "FROM VENDA V1, CLIENTE C1, PEDVENDA P1"
                + " WHERE V1.PEDVENCOD = P1.PEDVENCOD AND P1.CLICOD = C1.CIDCOD " + pedido + nome;
        return sql;

    }

